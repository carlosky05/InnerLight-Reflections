/* --- SISTEMA DE IDIOMA --- */

function detectarIdiomaUsuario() {
  if (!localStorage.getItem("idioma")) {
    const userLang = navigator.language || navigator.userLanguage;
    const langCode = userLang.substring(0, 2).toLowerCase();
    const idioma = (langCode === "en" || langCode === "es") ? langCode : "es";
    localStorage.setItem("idioma", idioma);
  }
}

function aplicarIdioma() {
  const idioma = localStorage.getItem("idioma") || "es";
  document.documentElement.lang = idioma;

  document.querySelectorAll("[data-key]").forEach(el => {
    const key = el.dataset.key;
    if (traducciones[idioma][key]) {
      el.textContent = traducciones[idioma][key];
    }
  });

  const toggle = document.getElementById("lang-toggle");
  if (toggle) {
    toggle.textContent = idioma === "es" ? "English" : "Español";
  }
}

function cambiarIdioma() {
  const actual = localStorage.getItem("idioma") || "es";
  localStorage.setItem("idioma", actual === "es" ? "en" : "es");
  aplicarIdioma();
}

window.addEventListener("DOMContentLoaded", () => {
  detectarIdiomaUsuario();
  aplicarIdioma();
  document.getElementById("lang-toggle")?.addEventListener("click", cambiarIdioma);
});
